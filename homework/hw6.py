# Библиотека colorama используется для вывода цветного текста в консоль.
# Она помогает визуально выделять сообщения, ошибки и уведомления.

from colorama import init, Fore, Style

init()

print(Fore.GREEN + "Успешное выполнение программы!")
print(Fore.YELLOW + "Предупреждение: это пример использования colorama.")
print(Fore.RED + "Ошибка: это просто демонстрация цвета.")
print(Style.RESET_ALL + "Обычный текст без цвета.")

